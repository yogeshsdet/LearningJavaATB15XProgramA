package ex_04_Operators;

public class Lab040_Relational_Operators {
    public static void main(String[] args) {
        int a = 10;
        int b = 30;
        boolean c = a > b;
        System.out.println(c);

        int age_mamitha = 33;
        int age_yogesh = 41;
        boolean result = age_yogesh >= age_mamitha;
        System.out.println(result);
    }
}
