package calculator;

public class Multi {
    //Multiplication program
    public void multiplication(int a, int b){
        int c = a * b;
        System.out.println("Multiplication for two values: " + c);
    }
    public void multiplication1(int a, int b, int c){
        int d = a * b * c;
        System.out.println("Multiplication for three values: " + d);
    }
    public void multiplication2(double a, double b){
        double c = a * b;
        System.out.println("Multiplication of decimal values for two values: " + c);
    }
    public void multiplication3(double a, double b, double c){
        double d = a * b * c;
        System.out.println("Multiplication of decimal values for three values: " + d);
    }
}
