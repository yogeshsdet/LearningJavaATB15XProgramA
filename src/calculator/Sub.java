package calculator;

public class Sub {
    public void subtraction(int a, int b){
        int c = a - b;
        System.out.println("Substraction of integer: " + c);
    }
    public void subtraction1(int a, int b, int c) {
        int d = a + b + c;
        System.out.println("Substraction of integer: " + d);
    }
    public void subtraction2(double i, double j){
        double k = i - j;
        System.out.println("Substraction of decimal: " + k);
    }
    public void subtraction3(double i, double j, double k){
        double l = i - j - k;
        System.out.println("Substraction of decimal: " + l);
    }
}
